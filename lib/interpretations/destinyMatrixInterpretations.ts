// Build the full array with numbers
export const ARCANA_INTERPRETATIONS: ArcanaInterpretation[] = arcanaData.map((data, index) => ({
  number: index + 1,
  ...data,
}));

/**
 * Get interpretation for specific arcana number (1-22)
 */
export function getArcanaInterpretation(number: number): ArcanaInterpretation | undefined {
  return ARCANA_INTERPRETATIONS.find(arcana => arcana.number === number);
}

/**
 * Get specific field from arcana interpretation
 */
export function getArcanaField(
  number: number,
  field: keyof Omit<ArcanaInterpretation, 'number'>
): string {
  const arcana = getArcanaInterpretation(number);
  return arcana?.[field] || '';
}

/**
 * Get all unique arcana numbers from a list (removes duplicates)
 */
export function getUniqueArcana(numbers: number[]): number[] {
  return Array.from(new Set(numbers)).sort((a, b) => a - b);
}
